			<h2 class="title"><?=lang('defualt_menu_ambiente_florestal'); ?></h2>

			<p>Por pertencer a uma cadeia de base florestal, a sustentabilidade das operações da CMPC Celulose Riograndense depende diretamente do uso responsável dos recursos da natureza. É por isso que a empresa maneja seus plantios de forma a minimizar e mitigar os impactos e promover o equilíbrio ambiental, levando em consideração os aspectos legais, técnicos, científicos, econômicos e sociais. Além de cumprir a legislação ambiental, a CMPC Celulose Riograndense mantém um Plano de Manejo Florestal com diretrizes específicas para cada uma de suas propriedades, desde o planejamento do plantio e da colheita, até a entrega da madeira na fábrica.</p>

			<p>A empresa possui mais de 800 propriedades, distribuídas em 57 municípios. A área total soma 324 mil hectares, sendo 170 mil hectares de efetivo plantio de eucalipto. Com isso, outros 154 mil hectares são preservados, caracterizados como áreas nativas, de preservação permanente e ou reserva legal.</p>


			<h3 id="pesquisa-e-monitoramento">Pesquisa e Monitoramento</h3>

			<h4>Monitoramento Ambiental</h4>

			<strong>Flora</strong>
			<p>Tipo de vegetação nativa existente na área da empresa são mapeados e fragmentos em estágio de regeneração inicial são caracterizadas no campo a fim de definir medidas de recuperação. Como o plantio é distribuído em diferentes biomas, estudos de caracterização para o monitoramento dinâmica fitossociológico são realizados em dois grandes blocos - um relacionado à vegetação florestal com tamanho e outros relacionados com a vegetação campestre. </p>

			<strong>Fauna</strong>
			<p>Os levantamentos de fauna da empresa são focados em aves, considerando os diversos tipos de ecossistemas existentes. A avifauna é o grupo bioindicador utilizado pela empresa para monitorar os impactos das atividades de manejo florestal sobre a biodiversidade das suas áreas de atuação. Gradualmente, outros grupos, como mamíferos, répteis e anfíbios que também são estudados para viabilizar a melhoria nas ações de conservação.   </p>

			<strong>Recursos Hídricos</strong>
			<p>Qualidade e quantidade da água são monitoradas em uma rede de microbacias que caracterizam as diferentes regiões onde a empresa atua dentro uma rede de equipamentos específicos (piezômetros), instalados para monitorar a profundidade dos lençóis freáticos e da qualidade da água subsuperficial. </p>

			<strong>Uso de aves como bioindicaroes</strong>
			<p>O comportamento das aves funciona como um termômetro da biodiversidade nos plantios da Celulose Riograndense, uma vez que elas são muito sensíveis e são as primeiras a ir embora a qualquer sinal de desequilíbrio. Graças a este processo a empresa conseguiu identificar mais de 200 espécies de aves em suas áreas.</p>



