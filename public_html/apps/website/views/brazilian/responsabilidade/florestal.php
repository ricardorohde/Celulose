			<h4>Monitoramento Ambiental</h4>

			<strong>Flora</strong>
			<p>Tipos de vegetação nativa existente na área da empresa são mapeados e fragmentos em estágio de regeneração inicial são caracterizadas no campo a fim de definir medidas de recuperação. Como o plantio é distribuído em diferentes biomas, estudos de caracterização para o monitoramento dinâmica fitossociológico são realizadas em dois grandes blocos - um relacionado à vegetação florestal com tamanho e outros relacionados com a vegetação campestre fazer.</p>

			<strong>Fauna</strong>
			<p>Os levantamentos de fauna da empresa são, basicamente focado em aves, considerando os diversos tipos de ecossistemas existentes. A avifauna é o grupo bioindicador utilizado pela empresa para monitorar os impactos das atividades de manejo florestal sobre a biodiversidade das suas áreas de atuação. Gradualmente, outros grupos, como mamíferos, répteis e anfíbios serão também estudados para viabilizar a melhoria nas ações de conservação.  </p>

			<strong>Recursos Hídricos</strong>
			<p>Qualidade e quantidade da água são monitoradas em uma rede de microbacias que caracterizam as diferentes regiões onde a empresa atua dentro Uma rede de equipamentos específicos (piezômetros) foi instalado para monitorar a profundidade dos lençóis freáticos e da qualidade da água subsuperficial. </p>

			<strong>Uso de aves como bioindicaroes</strong>
			<p>Comportamento das aves funciona como um termômetro da biodiversidade nos plantios de Celulose Riograndense, uma vez que eles são muito sensíveis e são os primeiros a ir embora a qualquer sinal de desequilíbrio. Na sua monitoramento do estado, a empresa já identificou mais de 200 espécies de aves em suas áreas. Pássaros freqüentes fragmentos de vegetação nativa entre os plantios de eucalipto alternados, além de muito eucalipto. </p>



